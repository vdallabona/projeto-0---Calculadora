namespace introCSharp{
    public class Operacoes{
        public static float Somar(float num1, float num2){
            
            float resultadoSoma;
            resultadoSoma = num2 + num1;
            return resultadoSoma;
        }
        public static float Subtrair(float num1, float num2){
            
            float resultadoSubtracao;
            resultadoSubtracao = num2 - num1;
            return resultadoSubtracao;
        }
        public static float Multiplicar(float num1, float num2){
            
            float resultadoMultiplicacao;
            resultadoMultiplicacao = num2 * num1;
            return resultadoMultiplicacao;
        }
        public static float Dividir(float num1, float num2){
            
            float resultadoDivisao;
            resultadoDivisao = num2 / num1;
            return resultadoDivisao;
        }
    }
}